# Main
# ---------------------------------------------------------------------------

def main():
    print("=" * 60)
    print("IPI Consent Decree — Seed Data Loader")
    print("=" * 60)

    records = build_records()
    print(f"\nPrepared {len(records)} seed consent decree records")

    # Summary by urgency tier
    tiers = {}
    for r in records:
        t = r["urgency_tier"]
        tiers[t] = tiers.get(t, 0) + 1
    print("\nUrgency tier breakdown:")
    for tier in ["OVERDUE", "CRITICAL", "ACTIVE", "MONITORING"]:
        if tier in tiers:
            print(f"  {tier:12s}: {tiers[tier]}")

    # Summary by state
    states = {}
    for r in records:
        states[r["state"]] = states.get(r["state"], 0) + 1
    print(f"\nStates covered: {len(states)}")
    print(f"  {', '.join(sorted(states.keys()))}")

    # Total penalties
    total_penalty = sum(r["penalty_amount"] for r in records)
    print(f"\nTotal penalty amounts: ${total_penalty:,.2f}")

    print("\nLoading to BigQuery...")
    rows = load_to_bigquery(records)

    print("\n" + "=" * 60)
    print(f"DONE — {rows} rows merged into BigQuery")
    print("=" * 60)


if __name__ == "__main__":
    main()
