def build_records() -> list:
    """Compute days_to_deadline, urgency_tier, and last_updated."""
    records = []
    for r in RAW_DECREES:
        days, tier = calc_urgency(r["compliance_end_date"])
        rec = dict(r)
        rec["days_to_deadline"] = days
        rec["urgency_tier"] = tier
        rec["case_status"] = "Active"
        rec["last_updated"] = NOW_TS
        records.append(rec)
    return records


# ---------------------------------------------------------------------------
# BigQuery MERGE (same pattern as etl.py)
# ---------------------------------------------------------------------------


def load_to_bigquery(records: list):
    """MERGE seed records into BigQuery using temp-table + MERGE pattern."""
    from google.cloud import bigquery

    project_id = os.getenv("GCP_PROJECT_ID")
    if not project_id:
        raise RuntimeError("GCP_PROJECT_ID not set")

    client = bigquery.Client(project=project_id)
    dataset_id = f"{project_id}.ipi_intelligence"
    table_id = f"{dataset_id}.consent_decrees"

    # Ensure dataset + table exist
    dataset = bigquery.Dataset(dataset_id)
    dataset.location = "US"
    client.create_dataset(dataset, exists_ok=True)
    print(f"  Dataset {dataset_id} ready")

    schema = [bigquery.SchemaField(name, dtype) for name, dtype in BQ_SCHEMA]
    table = bigquery.Table(table_id, schema=schema)
    client.create_table(table, exists_ok=True)

    # Schema evolution
    existing_table = client.get_table(table_id)
    existing_names = {f.name for f in existing_table.schema}
    new_fields = [f for f in schema if f.name not in existing_names]
    if new_fields:
        updated_schema = list(existing_table.schema) + new_fields
        existing_table.schema = updated_schema
        client.update_table(existing_table, ["schema"])
        print(f"  Updated table schema with {len(new_fields)} new columns")

    print(f"  Table {table_id} ready")

    # Temp table + MERGE
    temp_table_id = f"{dataset_id}._temp_seed_{int(time.time())}"
    temp_table = bigquery.Table(temp_table_id, schema=schema)
    client.create_table(temp_table, exists_ok=True)

    errors = client.insert_rows_json(temp_table_id, records)
    if errors:
        client.delete_table(temp_table_id, not_found_ok=True)
        raise RuntimeError(f"BigQuery insert errors: {errors}")

    merge_cols = [name for name, _ in BQ_SCHEMA if name != "case_number"]
    update_clause = ", ".join(f"T.{c} = S.{c}" for c in merge_cols)
    insert_cols = ", ".join(name for name, _ in BQ_SCHEMA)
    insert_vals = ", ".join(f"S.{name}" for name, _ in BQ_SCHEMA)

    merge_sql = f"""
    MERGE `{table_id}` T
    USING `{temp_table_id}` S
    ON T.case_number = S.case_number
    WHEN MATCHED THEN
        UPDATE SET {update_clause}
    WHEN NOT MATCHED THEN
        INSERT ({insert_cols})
        VALUES ({insert_vals})
    """

    print("  Executing MERGE...")
    query_job = client.query(merge_sql)
    query_job.result()
    rows_affected = query_job.num_dml_affected_rows or 0
    print(f"  MERGE complete — {rows_affected} rows affected")

    client.delete_table(temp_table_id, not_found_ok=True)
    return rows_affected


