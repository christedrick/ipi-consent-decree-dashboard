# Reference date for days_to_deadline calculation
# ---------------------------------------------------------------------------

TODAY = date(2026, 3, 25)
NOW_TS = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")


def calc_urgency(compliance_end_date_str: str) -> tuple:
    """Return (days_to_deadline, urgency_tier) based on compliance_end_date."""
    end = date.fromisoformat(compliance_end_date_str)
    days = (end - TODAY).days
    if days < 0:
        tier = "OVERDUE"
    elif days <= 365:
        tier = "CRITICAL"
    elif days <= 1095:
        tier = "ACTIVE"
    else:
        tier = "MONITORING"
    return days, tier


